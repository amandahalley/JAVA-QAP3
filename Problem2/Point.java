package Problem2;

public class Point {
    private float x; //x coordinate
    private float y; //y coordinate

    //Parameterized constructor 
    public Point(float x, float y) {
    this.x = x;
    this.y = y;
    }

    //No-argument constructor
    public Point() {
        x = 0.0f;
        y = 0.0f;
    }

    
}
